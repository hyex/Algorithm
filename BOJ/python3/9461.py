p = [0, 1, 1, 1, 2, 2, 3, 4, 5, 7, 9]

"""
[4] 1 + 1 = 2
[6] 2 + 1 = 3
----------------------
[7] 3 + 1 = 4 = [6]+[2]
[8] 4 + 1 = 5 = [7]+[3]
[9] 5 + 2 = 7 = [3]+[4]
[10] 7 + 2 = 9
[11] 9 + 3 = 12

[i-1]+[i-5]
"""

T = int(input())

for _ in range(T):
    N = int(input())
    if len(p) <= N:
        for i in range(len(p), N+1):
            p.append(p[i-1]+p[i-5])
    print(p[N])
